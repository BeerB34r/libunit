#include <framework.h>

// tests:
int	ok_launcher(void);
int	ko_launcher(void);

// launcher:
int	framework_tester_launcher(void)
{
	t_unit_ctx	*tests;

	tests = create_ctx("Framework");
	load_test(&tests, (t_test){
		.name = "OK",
		.func = &ok_launcher
	});
	load_test(&tests, (t_test){
		.name = "KO",
		.func = &ko_launcher
	});
	return (launch_tests(&tests));
}
