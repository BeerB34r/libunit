#include <framework.h>

// tests:
int	ok_test(void);
int	ko_test(void);
int	segv_test(void);
int	bus_test(void);
int	abrt_test(void);
int	fpe_test(void);
int	pipe_test(void);
int	ill_test(void);
int	time_test(void);
int	err_test(void);

// launcher:
int	framework_launcher(void)
{
	t_unit_ctx	*tests;

	tests = create_ctx("Framework");
	load_test(&tests, (t_test){
		.name = "OK",
		.func = &ok_test
	});
	load_test(&tests, (t_test){
		.name = "KO",
		.func = &ko_test
	});
	load_test(&tests, (t_test){
		.name = "SEGV",
		.func = &segv_test
	});
	load_test(&tests, (t_test){
		.name = "BUS",
		.func = &bus_test
	});
	load_test(&tests, (t_test){
		.name = "ABRT",
		.func = &abrt_test
	});
	load_test(&tests, (t_test){
		.name = "FPE",
		.func = &fpe_test
	});
	load_test(&tests, (t_test){
		.name = "PIPE",
		.func = &pipe_test
	});
	load_test(&tests, (t_test){
		.name = "ILL",
		.func = &ill_test
	});
	load_test(&tests, (t_test){
		.name = "TIME",
		.func = &time_test
	});
	load_test(&tests, (t_test){
		.name = "ERR",
		.func = &err_test
	});
	return (launch_tests(&tests));
}
