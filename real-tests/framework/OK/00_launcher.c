#include <framework.h>

// tests:
int	ok_return_test(void);
int	ok_stdout_capture_test(void);

// launcher:
int	ok_launcher(void)
{
	t_unit_ctx	*tests;

	tests = create_ctx("OK");
	load_test(&tests, (t_test){
		.name = "Return value",
		.func = &ok_return_test
	});
	load_test(&tests, (t_test){
		.name = "Stdout capture",
		.func = &ok_stdout_capture_test
	});
	return (launch_tests(&tests));
}
