#include <framework.h>

// test suites:
int	framework_launcher(void);
int	printf_launcher(void);
int	split_launcher(void);

int	main(void)
{
	t_unit_ctx	*tests;

	tests = create_ctx("All the tests");
	load_test(&tests, (t_test){
		.name = "Framework",
		.func = &framework_launcher
	});
	load_test(&tests, (t_test){
		.name = "Printf",
		.func = &printf_launcher
	});
	load_test(&tests, (t_test){
		.name = "Split",
		.func = &split_launcher
	});
	return (launch_tests(&tests));
}
