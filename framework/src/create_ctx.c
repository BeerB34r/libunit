#include <framework.h>
#include <stdlib.h>

void	*ft_calloc(size_t size, size_t nmemb);

t_unit_ctx	*create_ctx(char *function_name)
{
	t_unit_ctx *const	head = ft_calloc(sizeof(t_unit_ctx), 1);

	if (!head)
		return (NULL);
	head->test.name = function_name;
	return (head);
}
