#include <framework.h>
#include <stdlib.h>

void	free_ctx(t_unit_ctx *ctx)
{
	t_unit_ctx	*next;

	while (ctx)
	{
		next = ctx->next;
		free(ctx);
		ctx = next;
	}
}
