#include <libft.h>
#include <framework.h>

void	time_logger(t_test test, const int fd)
{
	ft_dprintf(fd,
		"[TIME]"
		"%s failed because it could not finish executing in %zu second(s).\n",
		test.name,
		test.timeout_seconds
		);
}

