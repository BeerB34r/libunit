#include <framework.h>

#define SUPPORTED_STATUS_COUNT 9

const static struct s_status
{
	t_status	status;
	char		*str;
}	g_status[SUPPORTED_STATUS_COUNT] = {
	{ERR, C_ERR},
	{OK, C_OK},
	{KO, C_KO},
	{SEGV, C_SEGV},
	{BUS, C_BUS},
	{ABRT, C_ABRT},
	{FPE, C_FPE},
	{PIPE, C_PIPE},
	{ILL, C_ILL}
};

char	*status(t_status status)
{
	unsigned int	i;

	i = -1;
	while (++i < SUPPORTED_STATUS_COUNT)
	{
		if (g_status[i].status == status)
			return (g_status[i].str);
	}
	return (NULL);
}
