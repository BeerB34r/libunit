#include <framework.h>
#include <stdlib.h>

void	*ft_calloc(size_t size, size_t nmemb);

void	load_test(t_unit_ctx **head, t_test test)
{
	t_unit_ctx *const	node = ft_calloc(sizeof(t_unit_ctx), 1);

	if (!node)
	{
		free_ctx(*head);
		return ;
	}
	else if (!*head)
	{
		free(node);
		return ;
	}
	node->test = test;
	get_last(*head)->next = node;
}
