#include <ft_printf.h>
#include <framework.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/wait.h>

static
void	run_child(t_testfunc func, t_unit_ctx *head)
{
	free_ctx(head);
	exit(func());
}

// TODO: timeout functionality
// probably requires waitpid with WNOHANG + sleep of some sort, i.e.
// philos/codexion
void	run_test(t_test	*test, t_unit_ctx *head)
{
	const pid_t	proc = fork();
	int			stat;
	int			retval;

	if (proc == (pid_t)-1)
	{
		test->status = ERR;
		return ;
	}
	if (proc == 0)
		run_child(test->func, head);
	while (wait(&stat) != proc)
		;
	if (WIFEXITED(stat))
	{
		retval = WEXITSTATUS(stat);
		test->status = map_retval_status(retval);
		return ;
	}
	else if (WIFSIGNALED(stat))
		test->status = map_signal_status(WTERMSIG(stat));
	else
		test->status = ERR;
}
