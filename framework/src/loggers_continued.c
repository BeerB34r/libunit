#include <ft_printf.h>
#include <framework.h>

void	abrt_logger(t_test test, const int fd)
{
	ft_dprintf(fd,
		"[ABRT]"
		"%s failed due to recieving the signal SIGABRT.\n",
		test.name
		);
}

void	pipe_logger(t_test test, const int fd)
{
	ft_dprintf(fd,
		"[PIPE]"
		"%s failed due to recieving the signal SIGPIPE.\n",
		test.name
		);
}
