#ifndef TYPES_H
# define TYPES_H

# include <stdlib.h>
# include <stdbool.h>

typedef enum e_status		t_status;
typedef int					(*t_testfunc)(void);
typedef struct s_test		t_test;
typedef struct s_test_list	t_unit_ctx;

enum e_status
{
	UNKNOWN,
	ERR,
	OK,
	KO,
	SEGV,
	BUS,
	ABRT,
	FPE,
	PIPE,
	ILL,
	TIME
};

struct s_test
{
	char		*name;
	t_testfunc	func;
	bool		silent;
	t_status	status;
	size_t		timeout_seconds;
	t_status	expected;
};
struct s_test_list
{
	t_test		test;
	t_unit_ctx	*next;
};
#endif // TYPES_H
