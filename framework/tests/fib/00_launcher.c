#include <framework.h>

// tests:
int	fib_basic_test(void);
int	fib_negative_test(void);
int	fib_bignum_test(void);

// launcher:
int	fib_launcher(void)
{
	t_unit_ctx	*tests;

	tests = create_ctx("fib()");
	load_test(&tests, (t_test){
		.name = "Basic",
		.func = &fib_basic_test,
		.timeout_seconds = 1
	});
	load_test(&tests, (t_test){
		.name = "Negative",
		.func = &fib_negative_test,
		.timeout_seconds = 1
	});
	load_test(&tests, (t_test){
		.name = "Big number",
		.func = &fib_bignum_test,
		.timeout_seconds = 1
	});
	return (launch_tests(&tests));
}
