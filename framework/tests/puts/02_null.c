#include <framework.h>

// function to be tested:
size_t	puts(const char *str);

static
int	check_return_null(void)
{
	if (puts(NULL) == 6)
		return (0);
	return (1);
}

// test function:
int	puts_null_test(void)
{
	int	retval;

	retval = printing_test("(null)", "", &check_return_null);
	if (retval)
		return (retval);
	return (0);
}
