// function to be tested:
int	ft_strlen(char *str);

// test function:
int	basic_test(void)
{
	if (ft_strlen("string with length 21") != 21)
		return (1);
	if (ft_strlen("") != 0)
		return (1);
	if (ft_strlen("Hello, World!\n") != 14)
		return (1);
	return (0);
}
