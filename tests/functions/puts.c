#include <stddef.h>
#include <unistd.h>

size_t	puts(const char *s)
{
	size_t	accumulator;

	accumulator = 0;
	while (*s)
		accumulator += write(STDOUT_FILENO, s++, 1);
	return (accumulator);
}
