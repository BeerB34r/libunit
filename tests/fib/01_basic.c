int	fib_basic_test(void)
{
	if (fib(0) != 0)
		return (1);
	if (fib(1) != 1)
		return (1);
	if (fib(2) != 1)
		return (1);
	if (fib(3) != 2)
		return (1);
	if (fib(4) != 3)
		return (1);
	if (fib(5) != 5)
		return (1);
	return (0);
}
